                        <p class="main-article-p">
                            For most of American history, financial policy was a central political battleground. There was the feud between Thomas Jefferson and Alexander Hamilton over Revolutionary War debt; the Whiskey Rebellion; Andrew Jackson’s assault on the Second Bank of The United States; the greenbacks Abraham Lincoln issued to help finance the Civil War; William Jennings Bryan and the cross of gold; the creation of the Federal Reserve; FDR’s New Deal. 
                            These were among the most heated political issues of their day. And they were all understood to be questions of power and democratic accountability, not merely matters of growth or efficiency.
                            <br>
                            But beginning in the 1950s, the United States increasingly came to understand finance as apolitical ― something best handled by technocratic experts insulated from the passions of a democratic electorate. This idea went by different slogans ― “the liberal consensus,” “the great moderation,” “central bank independence” ― but they all amounted to the same thing: The economy was nonideological. 
                            The decisions made by experts tending to the financial machine were were strictly tactical. Any mistakes were a matter of pulling the wrong lever or setting a dial too high. 
                        </p>

                        <p class="main-article-p">
                            The financial crisis exploded this myth. “The failures of the crash and the bailout were not technocratic failures,” says University of Georgia law professor Mehrsa Baradaran. “They were about power.”
                        </p>

                        <p class="main-article-p">
                            Lehman would be the only major American financial institution to out-and-out fail in the crisis. Everyone else was bailed out on generous terms that not only protected their creditors, but their shareholders and ― with the exception of AIG ― the jobs of their top executives. Criminals who broke the law were shielded from prosecution.
                            <br>
                            Here’s what happened to everyone who didn’t work for a bank: As a percentage of each family’s overall wealth, the poorer you were, the more you lost in the crash. The top 1 percent of U.S. households ultimately captured more than half of the economic gains over the course of the Obama years, while the bottom 99 percent never recovered their losses from the crash.
