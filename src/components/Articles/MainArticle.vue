                            According to Stanford University Business School Professor Anat Admati, the 19 biggest American banks passed out $80 billion in dividends between the summer of 2007 and the close of 2008. They drew $160 billion in bailout funds from the U.S. Treasury, and untold billions from the Fed’s $7.7 trillion in emergency lending. 
